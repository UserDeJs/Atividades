let var1 = true;
let var2 = false;

let andResulto = var1 && var2;
console.log("Resultado de AND (var1 && var2):", andResulto);

let orResulto = var1 || var2;
console.log("Resultado de OR (var1 || var2):", orResulto);

let notVar1 = !var1;
let notVar2 = !var2;
console.log("Resultado de NOT (!var1):", notVar1);
console.log("Resultado de NOT (!var2):", notVar2);